/** Interpola `{a}`/`{b}`/`{name}` en claves con placeholder. */
export function gmFill(template: string, vars: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? ''));
}
